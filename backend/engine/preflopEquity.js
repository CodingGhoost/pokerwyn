const RANK_VALUE = {
    'A': 14, 'K': 13, 'Q': 12, 'J': 11, 'T': 10, '10': 10,
    '9': 9, '8': 8, '7': 7, '6': 6, '5': 5, '4': 4, '3': 3, '2': 2
};

/**
 * Converts a hand (e.g., ["As", "Kd"]) into a lookup key (e.g., "AKo").
 * @param {Array} hand - Array of two card strings (e.g. ['As', 'Th'] or ['10s', '2s'])
 * @returns {String} - The key for the equity table (e.g., "ATo", "JTs", "88")
 */
function getHandKey(hand) {
    if (!hand || hand.length !== 2) return null;

    // Parse Cards
    const parseCard = (cardStr) => {
        let rank = cardStr.slice(0, -1);
        let suit = cardStr.slice(-1);
        
        if (rank === '10') rank = 'T';
        
        return { rank, suit, value: RANK_VALUE[rank] };
    };

    const c1 = parseCard(hand[0]);
    const c2 = parseCard(hand[1]);

    // Sort by Rank
    let high, low;
    if (c1.value > c2.value) { high = c1; low = c2; }
    else { high = c2; low = c1; }

    // Construct key
    if (high.value === low.value) {
        return `${high.rank}${low.rank}`;
    } else if (high.suit === low.suit) {
        return `${high.rank}${low.rank}s`;
    } else {
        return `${high.rank}${low.rank}o`;
    }
}

/**
 * Looks up preflop equity based on hand and number of active opponents.
 * @param {Array} hand - Array of two card strings
 * @param {Number} activePlayerCount - Total players in the hand (including hero)
 * @returns {Number} - The equity percentage (0-100)
 */

function getPreflopEquity(hand, activePlayerCount) {
    const key = getHandKey(hand);
    
    const opponents = Math.max(1, Math.min(8, activePlayerCount - 1));

    if (preflopEquity[key]) {
        return preflopEquity[key][opponents];
    }
    
    return 0;
}

